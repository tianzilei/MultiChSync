"""
Marker information extraction and reporting module for MultiChSync.

This module extracts metadata and statistics from marker CSV files
generated by the MultiChSync pipeline, and generates comprehensive reports.
"""

import re
from pathlib import Path

import pandas as pd
import numpy as np
from typing import Union, Optional, List, Dict, Tuple
import logging

# =========================================================
# Configuration
# =========================================================
# Default column names for marker CSV files
# These will be auto-detected from actual file headers
POSSIBLE_TIME_COLS = ["reference_time", "Time(sec)", "time", "Time", "referencetime"]
POSSIBLE_VALUE_COLS = ["value", "Value"]
POSSIBLE_INDEX_COLS = ["index", "Index", ""]  # empty string for unnamed index


